#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		c += ('a' - 'A');
	return (c);
}

bool	ft_islower(int c)
{
	return (c >= 'a' && c <= 'z');
}
