#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (!lst || !new)
		return ;
	new->prev = NULL;
	new->next = *lst;
	if (*lst)
		(*lst)->prev = new;
	*lst = new;
}
